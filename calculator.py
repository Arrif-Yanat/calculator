# นำเข้าโมดูล math (ในกรณีต้องการใช้งานฟังก์ชันคำนวณเพิ่มเติม)
import math

# ฟังก์ชันแสดงเมนูหลัก
def display_menu():
    print("เลือกการคำนวณ:")
    print("1. บวก")
    print("2. ลบ")
    print("3. คูณ")
    print("4. หาร")
    print("5. ยกกำลัง")
    print("6. ออกจากโปรแกรม")

# ฟังก์ชันการคำนวณแต่ละประเภท
def calculator():
    while True:  # ใช้ลูปเพื่อทำงานซ้ำจนกว่าผู้ใช้จะเลือกออก
        display_menu()  # แสดงเมนู
        choice = input("ป้อนหมายเลขที่ต้องการ: ")  # รับอินพุตจากผู้ใช้
        
        # ตรวจสอบว่าเลือกออกหรือไม่
        if choice == '6':
            print("ขอบคุณที่ใช้โปรแกรม!")
            break  # ออกจากลูป
        
        # บอกว่าต้องใส่ตัวเลขสองตัว
        if choice in ['1', '2', '3', '4', '5']:
            num1 = int(input("ป้อนตัวเลขที่ 1: "))  # รับตัวเลขที่ 1
            num2 = int(input("ป้อนตัวเลขที่ 2: "))  # รับตัวเลขที่ 2
            
            # การดำเนินการแต่ละประเภท
            if choice == '1':  # บวก
                print(f"ผลลัพธ์: {num1} + {num2} = {num1 + num2}")
            elif choice == '2':  # ลบ
                print(f"ผลลัพธ์: {num1} - {num2} = {num1 - num2}")
            elif choice == '3':  # คูณ
                print(f"ผลลัพธ์: {num1} * {num2} = {num1 * num2}")
            elif choice == '4':  # หาร
                if num2 != 0:  # ตรวจสอบว่าเลขตัวที่สองไม่ใช่ศูนย์
                    print(f"ผลลัพธ์: {num1} / {num2} = {num1 / num2}")
                else:
                    print("ไม่สามารถหารด้วยศูนย์ได้")
            elif choice == '5':  # ยกกำลัง
                print(f"ผลลัพธ์: {num1} ^ {num2} = {math.pow(num1, num2)}")
        
        # กรณีป้อนตัวเลือกไม่ถูกต้อง
        else:
            print("โปรดเลือกหมายเลขที่ถูกต้อง (1-6)")

# เริ่มโปรแกรม
calculator()